# 숫자형을 활용한 연산자
# 사칙연산 : + , - , * , / , //(몫) , %(나머지) 

su1 = 7
su2 = 3

res = su1 + su2
print('더하기 : ' , res)

res = su1 - su2
print('빼기 : ' , res)

res = su1 * su2
print('곱하기 : ' , res)

res = su1 / su2
print('나누기 : ' , '%.2f' %res)

res = su1 // su2
print('몫 : ' , res)

res = su1 % su2
print('나머지 : ' , res)

print('ㅡ' * 30)

# 복합연산 : += , -= , *= , /= , //= , %= , **=
# 대입연산자
su1 = su1 + 10
print(su1)

su1 += 10
print(su1)




